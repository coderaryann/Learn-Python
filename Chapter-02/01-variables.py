# Rules of variable name
# 1. Variable name should be a alphabets, digits or underscores.
# 2. Variable name should not start with a digit. It's only start with an alphabet and underscores.
# 3. No while spaced is allowed to be used inside a variable name.

# 4. Variable name should not be a keyword in python. For example: if, else
# 5. Variable name should not be a python function name. For example: print, len
